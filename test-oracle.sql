CREATE OR REPLACE FUNCTION FORMAT_CURRENCY(
    P_AMOUNT  IN NUMBER,
    P_SYMBOL  IN VARCHAR2
) RETURN VARCHAR2
IS
BEGIN
    RETURN P_SYMBOL || TO_CHAR(P_AMOUNT, 'FM999,999,990.00');
END FORMAT_CURRENCY;
/

CREATE OR REPLACE FUNCTION IS_VALID_EMAIL(
    P_ADDR IN VARCHAR2
) RETURN NUMBER
IS
BEGIN
    IF REGEXP_LIKE(P_ADDR, '^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}$') THEN
        RETURN 1;
    END IF;
    RETURN 0;
END IS_VALID_EMAIL;
/

CREATE OR REPLACE FUNCTION CUSTOMER_LIFETIME_VALUE(
    P_CUSTOMER_ID IN NUMBER
) RETURN NUMBER
IS
    V_TOTAL NUMBER(14,2);
BEGIN
    SELECT NVL(SUM(TOTAL_AMOUNT), 0)
    INTO V_TOTAL
    FROM ORDERS
    WHERE CUSTOMER_ID = P_CUSTOMER_ID;
    RETURN V_TOTAL;
END CUSTOMER_LIFETIME_VALUE;
/

