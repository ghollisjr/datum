-- test-oracle.sql
-- Creates test tables, views, functions & procedures for exercising
-- datum's Oracle introspection features.
--
-- Runs as the DATUM_TEST app user in the FREEPDB1 pluggable database.
-- The gvenzl/oracle-free Docker image auto-creates this user via
-- APP_USER / APP_USER_PASSWORD environment variables.
--
-- Usage:
--   sqlplus datum_test/datum_test@oracle:1521/FREEPDB1 @test-oracle.sql

-- ============================================================
-- Tables
-- ============================================================

CREATE TABLE CUSTOMERS (
    CUSTOMER_ID   NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    FIRST_NAME    VARCHAR2(100) NOT NULL,
    LAST_NAME     VARCHAR2(100) NOT NULL,
    EMAIL         VARCHAR2(255) NULL,
    CREATED_AT    TIMESTAMP DEFAULT SYSTIMESTAMP,
    IS_ACTIVE     NUMBER(1) DEFAULT 1,
    CONSTRAINT UQ_CUSTOMERS_EMAIL UNIQUE (EMAIL)
);

CREATE TABLE PRODUCTS (
    PRODUCT_ID    NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    SKU           VARCHAR2(50) NOT NULL,
    NAME          VARCHAR2(200) NOT NULL,
    DESCRIPTION   CLOB NULL,
    CATEGORY      VARCHAR2(100) NULL,
    UNIT_PRICE    NUMBER(10,2) NOT NULL,
    CREATED_AT    TIMESTAMP DEFAULT SYSTIMESTAMP,
    CONSTRAINT UQ_PRODUCTS_SKU UNIQUE (SKU)
);

CREATE TABLE ORDERS (
    ORDER_ID      NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    CUSTOMER_ID   NUMBER NOT NULL,
    ORDER_DATE    DATE DEFAULT TRUNC(SYSDATE) NOT NULL,
    TOTAL_AMOUNT  NUMBER(12,2) DEFAULT 0 NOT NULL,
    STATUS        VARCHAR2(20) DEFAULT 'pending',
    CONSTRAINT FK_ORDERS_CUSTOMER
        FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMERS(CUSTOMER_ID)
);

CREATE TABLE ORDER_ITEMS (
    ITEM_ID       NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    ORDER_ID      NUMBER NOT NULL,
    PRODUCT_ID    NUMBER NOT NULL,
    QUANTITY      NUMBER DEFAULT 1 NOT NULL,
    UNIT_PRICE    NUMBER(10,2) NOT NULL,
    CONSTRAINT FK_ITEMS_ORDER
        FOREIGN KEY (ORDER_ID) REFERENCES ORDERS(ORDER_ID)
);

CREATE TABLE STOCK_LEVELS (
    STOCK_ID      NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    PRODUCT_ID    NUMBER NOT NULL,
    WAREHOUSE     VARCHAR2(100) NOT NULL,
    QUANTITY      NUMBER DEFAULT 0 NOT NULL,
    LAST_UPDATED  TIMESTAMP DEFAULT SYSTIMESTAMP,
    CONSTRAINT FK_STOCK_PRODUCT
        FOREIGN KEY (PRODUCT_ID) REFERENCES PRODUCTS(PRODUCT_ID)
);

CREATE TABLE SUPPLIERS (
    SUPPLIER_ID   NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    NAME          VARCHAR2(200) NOT NULL,
    CONTACT_EMAIL VARCHAR2(255) NULL,
    PHONE         VARCHAR2(50) NULL,
    COUNTRY       VARCHAR2(100) NULL
);

CREATE TABLE DAILY_SALES (
    REPORT_DATE   DATE PRIMARY KEY,
    TOTAL_ORDERS  NUMBER DEFAULT 0 NOT NULL,
    TOTAL_REVENUE NUMBER(14,2) DEFAULT 0 NOT NULL,
    AVG_ORDER     NUMBER(10,2) NULL
);

CREATE TABLE CUSTOMER_SEGMENTS (
    SEGMENT_ID    NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    SEGMENT_NAME  VARCHAR2(100) NOT NULL,
    MIN_SPEND     NUMBER(12,2) NULL,
    MAX_SPEND     NUMBER(12,2) NULL,
    DESCRIPTION   CLOB NULL
);

-- ============================================================
-- Views
-- ============================================================

CREATE OR REPLACE VIEW CUSTOMER_ORDER_SUMMARY AS
SELECT
    c.CUSTOMER_ID,
    c.FIRST_NAME || ' ' || c.LAST_NAME AS FULL_NAME,
    c.EMAIL,
    COUNT(o.ORDER_ID) AS ORDER_COUNT,
    NVL(SUM(o.TOTAL_AMOUNT), 0) AS LIFETIME_SPEND,
    MAX(o.ORDER_DATE) AS LAST_ORDER_DATE
FROM CUSTOMERS c
LEFT JOIN ORDERS o ON c.CUSTOMER_ID = o.CUSTOMER_ID
GROUP BY c.CUSTOMER_ID, c.FIRST_NAME, c.LAST_NAME, c.EMAIL;

CREATE OR REPLACE VIEW LOW_STOCK AS
SELECT
    p.PRODUCT_ID,
    p.SKU,
    p.NAME,
    sl.WAREHOUSE,
    sl.QUANTITY,
    sl.LAST_UPDATED
FROM PRODUCTS p
JOIN STOCK_LEVELS sl ON p.PRODUCT_ID = sl.PRODUCT_ID
WHERE sl.QUANTITY < 10;

CREATE OR REPLACE VIEW REVENUE_BY_CATEGORY AS
SELECT
    p.CATEGORY,
    COUNT(DISTINCT o.ORDER_ID) AS NUM_ORDERS,
    SUM(oi.QUANTITY) AS UNITS_SOLD,
    SUM(oi.QUANTITY * oi.UNIT_PRICE) AS TOTAL_REVENUE
FROM ORDER_ITEMS oi
JOIN ORDERS o ON oi.ORDER_ID = o.ORDER_ID
JOIN PRODUCTS p ON oi.PRODUCT_ID = p.PRODUCT_ID
GROUP BY p.CATEGORY;

