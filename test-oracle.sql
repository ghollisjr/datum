CREATE OR REPLACE PROCEDURE DEACTIVATE_CUSTOMER(
    P_CUSTOMER_ID IN NUMBER
)
IS
BEGIN
    UPDATE CUSTOMERS
    SET IS_ACTIVE = 0
    WHERE CUSTOMER_ID = P_CUSTOMER_ID;
END DEACTIVATE_CUSTOMER;
/

CREATE OR REPLACE PROCEDURE PLACE_ORDER(
    P_CUSTOMER_ID IN NUMBER,
    P_PRODUCT_ID  IN NUMBER,
    P_QUANTITY    IN NUMBER,
    P_STATUS      IN VARCHAR2 DEFAULT 'pending'
)
IS
    V_PRICE    NUMBER(10,2);
    V_ORDER_ID NUMBER;
BEGIN
    SELECT UNIT_PRICE INTO V_PRICE
    FROM PRODUCTS
    WHERE PRODUCT_ID = P_PRODUCT_ID;

    INSERT INTO ORDERS (CUSTOMER_ID, ORDER_DATE, TOTAL_AMOUNT, STATUS)
    VALUES (P_CUSTOMER_ID, TRUNC(SYSDATE), V_PRICE * P_QUANTITY, P_STATUS)
    RETURNING ORDER_ID INTO V_ORDER_ID;

    INSERT INTO ORDER_ITEMS (ORDER_ID, PRODUCT_ID, QUANTITY, UNIT_PRICE)
    VALUES (V_ORDER_ID, P_PRODUCT_ID, P_QUANTITY, V_PRICE);

    UPDATE STOCK_LEVELS
    SET QUANTITY = QUANTITY - P_QUANTITY,
        LAST_UPDATED = SYSTIMESTAMP
    WHERE PRODUCT_ID = P_PRODUCT_ID;
EXCEPTION
    WHEN NO_DATA_FOUND THEN
        RAISE_APPLICATION_ERROR(-20001, 'Product not found');
END PLACE_ORDER;
/

CREATE OR REPLACE PROCEDURE REBUILD_DAILY_SALES(
    P_START_DATE IN DATE,
    P_END_DATE   IN DATE
)
IS
BEGIN
    DELETE FROM DAILY_SALES
    WHERE REPORT_DATE BETWEEN P_START_DATE AND P_END_DATE;

    INSERT INTO DAILY_SALES (REPORT_DATE, TOTAL_ORDERS, TOTAL_REVENUE, AVG_ORDER)
    SELECT
        o.ORDER_DATE,
        COUNT(*),
        SUM(o.TOTAL_AMOUNT),
        AVG(o.TOTAL_AMOUNT)
    FROM ORDERS o
    WHERE o.ORDER_DATE BETWEEN P_START_DATE AND P_END_DATE
    GROUP BY o.ORDER_DATE;
END REBUILD_DAILY_SALES;
/

CREATE OR REPLACE PROCEDURE GET_CUSTOMER_STATS(
    P_CUSTOMER_ID IN  NUMBER,
    P_ORDER_COUNT OUT NUMBER,
    P_TOTAL_SPEND OUT NUMBER
)
IS
BEGIN
    SELECT COUNT(*), NVL(SUM(TOTAL_AMOUNT), 0)
    INTO P_ORDER_COUNT, P_TOTAL_SPEND
    FROM ORDERS
    WHERE CUSTOMER_ID = P_CUSTOMER_ID;
END GET_CUSTOMER_STATS;
/

